     */
    public function store(Request $request)
    {
        $validatedData = $request->validateWithBag('tags', [
            'name' => ['required','unique:tags', 'max:50'],
        ]); 
        $tag = new Tag;
        $tag->name = $request->name;

        if ($tag->save()) {
            return redirect('tag/create')->with('status', 'Tag Saved Succeessfully!');
        }else {
            return redirect('tag/create')->with('error', 'Data Not Saved!');
        }
    }

